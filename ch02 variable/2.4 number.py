#!/usr/bin/env python3

# 整数
num1 = 2
num2 = 3
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)
print(num1 ** num2) # 乘方

# 浮点数
float1 = 1.5
print(num1 + float1)

# 可以使用下划线将数字分组，更清晰易读
universe_age = 14_000_000
print(universe_age + 1)

# 可以同时给多个变量赋值
x,y,z = 1,2,3
print(x, y, z)

# python 中不能定义常量，全大写只是形式上像是常量
MAX_V = 5
MAX_V = 6
print(MAX_V)

